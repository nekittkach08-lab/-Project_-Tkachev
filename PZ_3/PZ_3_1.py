#Даны числа х, у, x1, y1, х2, у2. Проверить истинность высказывания: «Точка с
#координатами (х, у) лежит внутри прямоугольника, левая верхняя вершина которого
#имеет координаты (х1, у1), правая нижняя — (х2, у2), а стороны параллельны
#координатным осям».

x = float(input("Введите x: "))
y = float(input("Введите y: "))
x1 = float(input("Введите x1: "))
y1 = float(input("Введите y1: "))
x2 = float(input("Введите x2: "))
y2 = float(input("Введите y2: "))

if x1 > x2:
    x1, x2 = x2, x1
if y1 < y2:
    y1, y2 = y2, y1

inside = (x1 <= x <= x2) and (y2 <= y <= y1)

print(f"Точка ({x}, {y}) лежит внутри прямоугольника: {inside}")