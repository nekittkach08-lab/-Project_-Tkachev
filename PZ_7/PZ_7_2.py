#Дана строка, содержащая по крайней мере один символ пробела. Вывести подстроку,
#расположенную между первым и вторым пробелом исходной строки. Если строка
#содержит только один пробел, то вывести пустую строку.


s = input()  # ввод строки

# Находим позицию первого пробела
first_space = s.find(' ')
# Находим позицию второго пробела, начиная поиск после первого
second_space = s.find(' ', first_space + 1)

# Если второй пробел не найден, выводим пустую строку
if second_space == -1:
    print('')
else:
    # Выводим подстроку между первым и вторым пробелом
    print(s[first_space + 1:second_space])